
public class Gummibärchen {

	String farbe;
	int anzahl;
	
	public Gummibärchen(String farbe, int anzahl) {
		this.farbe = farbe;
		this.anzahl = anzahl;
	}
	
	public String getFarbe() {
		return farbe;
	}
	
	public void setFarbe(String farbe) {
		this.farbe = farbe;
	}
	
	public int getAnzahl() {
		return anzahl;
	}
	
	public void setAnzahl(int anzahl) {
		this.anzahl = anzahl;
	}
	
	
}
